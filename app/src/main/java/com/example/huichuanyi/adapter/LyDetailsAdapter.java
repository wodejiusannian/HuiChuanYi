public class LyDetailsAdapter extends BaseAdapter {

    private List<String> mData;
    private Context context;

    public LyDetailsAdapter(List<String> mData, Context context) {
        this.mData = mData;
        this.context = context;
    }

    @Override
    public int getCount() {
        return mData.size() == 0 ? 0 : mData.size();
    }

    @Override
    public Object getItem(int position) {
        return mData.get(position);
    }

    @Override
    public long getItemId(int position) {
        return position;
    }

    @Override
    public View getView(int position, View convertView, ViewGroup parent) {
        ViewHolder holder;
        if (convertView == null) {
            convertView = LayoutInflater.from(context).inflate(R.layout.item_ly_details, null);
            holder = new ViewHolder(convertView);
            convertView.setTag(holder);
        } else {
            holder = (ViewHolder) convertView.getTag();
        }
        holder.text.setText(mData.get(position));
        return convertView;
    }

    public static class ViewHolder {
        private TextView text;

        public ViewHolder(View v) {
            text = (TextView) v.findViewById(R.id.tv_ly_details_text);
        }
    }
}
